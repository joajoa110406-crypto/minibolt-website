/**
 * 주어진 우편번호가 도서산간 지역인지 판별
 * @param zipcode 5자리 우편번호 (예: "63000")
 * @returns true면 도서산간 지역
 */
export function isIslandAddress(zipcode: string): boolean {
  if (!zipcode) return false;

  // 숫자만 추출
  const cleaned = zipcode.replace(/\D/g, '');
  if (cleaned.length !== 5) return false;

  // 개별 우편번호 목록 체크
  if (ISLAND_INDIVIDUAL.has(cleaned)) return true;

  // 범위 체크
  const num = parseInt(cleaned, 10);
  for (const [start, end] of ISLAND_RANGES) {
    if (num >= start && num <= end) return true;
  }

  return false;
}

/**
 * 도서산간 추가 배송비 반환
 * @returns 도서산간 추가 배송비 (원)
 */
export function getIslandFee(): number {
  return ISLAND_FEE;
}
